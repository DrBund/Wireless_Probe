#define USI_SS                     PB5
#define USI_SS_PORT                PORTB
#define USI_SS_PIN                 PINB
#define USI_SS_DDR                 DDRB

#define USI_DI                     PB0
#define USI_DI_PORT                PORTB
#define USI_DI_PIN                 PINB
#define USI_DI_DDR                 DDRB

#define USI_DO                     PB1
#define USI_DO_PORT                PORTB
#define USI_DO_PIN                 PINB
#define USI_DO_DDR                 DDRB

#define USI_USCK                     PB2
#define USI_USCK_PORT                PORTB
#define USI_USCK_PIN                 PINB
#define USI_USCK_DDR                 DDRB


#endif //ATTINYpinDefines.h
